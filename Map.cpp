/// <summary>
/// 描画
/// </summary>
void Map::Draw()
{
	//行の繰り返し
	for (int y = 0; y < mapHeight_; y++)
	{
		//列の繰り返し
		for (int x = 0; x < mapWidth_; x++)
		{
			if (tutorialCopyMap[y][x] == BLOCK)
			{
				DrawBox(x - 16 * blockSize, y - 16 * blockSize, x + 16 * blockSize, y + 16 * blockSize, GetColor(255, 255, 255), true);
					
			}
		}
	}
}

